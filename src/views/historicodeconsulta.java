    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        if(agendamentos.getSelectedRow() != -1){
            
            HistoricoDeConsulta h = new HistoricoDeConsulta();
            
            h.setId((int)agendamentos.getValueAt(agendamentos.getSelectedRow(), 0));
            h.setProcedimento((String)agendamentos.getValueAt(agendamentos.getSelectedRow(), 2));
            h.setData((String)agendamentos.getValueAt(agendamentos.getSelectedRow(), 3));
            h.setHora((String)agendamentos.getValueAt(agendamentos.getSelectedRow(), 4));
            
            new HistoricoDeConsultaDAO().update(h);
            readJTable(pagina, qtd_pag);
            
        }
    }//GEN-LAST:event_jButton14ActionPerformed

    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        if (agendamentos.getSelectedColumn() != -1){
                int option = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?");
                if (option == JOptionPane.YES_OPTION){
                    int id = (int) agendamentos.getValueAt(agendamentos.getSelectedRow(), 0);
                    new HistoricoDeConsultaDAO().delete(id);
                    readJTable(pagina, qtd_pag);
                }
        }
    }//GEN-LAST:event_jButton15ActionPerformed

