    public void readJTable(int pag, int qp){        
        methods m = new methods(pag);

        m.disabledButtonDown(jButton10 , 1);
        m.disabledButtonDown(jButton12 , 2);

        DefaultTableModel modelo = (DefaultTableModel) agendamentos.getModel();
        HistoricoDeConsultaDAO hdao = new HistoricoDeConsultaDAO();
        int max = hdao.numPags(qp);
        
        m.disabledButtonUp(jButton13 , max-1);
        m.disabledButtonUp(jButton11 ,max);

        modelo.setNumRows(0);
        labelPagina.setText(Integer.toString(pag));

        for(HistoricoDeConsulta a: hdao.historicoConsultas(pag, qp)) {
            modelo.addRow(new Object[]{
                a.getId(),
                a.getPaciente(),
                a.getProcedimento(),
                a.getData(),
                a.getHora(),
                a.getBairro()
            });
        }
    }
    
    public void readJTableForNome(){

        DefaultTableModel modelo = (DefaultTableModel) agendamentos.getModel();
        HistoricoDeConsultaDAO hdao = new HistoricoDeConsultaDAO();

        modelo.setNumRows(0);

        for(HistoricoDeConsulta a: hdao.hcForPaciente(pesquisar.getText())) {
            modelo.addRow(new Object[]{
                a.getId(),
                a.getPaciente(),
                a.getProcedimento(),
                a.getData(),
                a.getHora(),
                a.getBairro()
            });
        }
    }

