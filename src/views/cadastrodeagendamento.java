    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String pac = (String) pacientes.getSelectedItem();
        int i = pacientes.getSelectedIndex();
        
        if(!area.getText().equals("") && !procedimento.getText().equals("") && !hora.getText().equals("") && !data.getText().equals("") && pac != null){
            Agendamento a = new Agendamento();

            a.setPaciente(pac);
            a.setArea(area.getText());
            a.setProcedimento(procedimento.getText());
            a.setHora(hora.getText());
            a.setData(data.getText());
            a.setStatus(status.getSelectedItem().toString());
            a.setIdpaciente((int) id.get(i));

            AgendamentoDAO adao = new AgendamentoDAO();      

            if(adao.agendar(a)){
                JOptionPane.showMessageDialog(null, "Agendado com sucesso!");
                paciente.setText("");
                area.setText("");
                pacientes.removeAllItems();
                procedimento.setText("");
                data();
            }else{
                JOptionPane.showMessageDialog(null, "Agendado com sucesso!");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        }
        
        new home().dispose();
                

    }//GEN-LAST:event_jButton1ActionPerformed

