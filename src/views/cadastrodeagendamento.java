    public void readPaciente(){
            
            if(!paciente.getText().equals("")){
                PacienteDAO pdao = new PacienteDAO();

                JComboBox modelo = pacientes;

                modelo.removeAllItems();           

                for (Paciente pe: pdao.select(paciente.getText())) {
                    
                    modelo.addItem(
                        pe.getNome()
                    );  

                }
                
                id.removeAll(id);
                
                for (Paciente pe: pdao.select(paciente.getText())) {
                
                    id.add(pe.getId());
                
                }
        }
    }
    
